     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        if( Auth::check() ) {
            $category = Category::find($id);
            return view('categories.show', ['category'=>$category]);
        }

        return view('auth.login');
    }

